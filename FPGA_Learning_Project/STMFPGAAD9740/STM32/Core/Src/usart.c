/* UART command buffer */
static char uart_rx_buf[64];
static volatile uint8_t uart_rx_idx = 0;
static volatile uint8_t uart_cmd_ready = 0;

void UART_Console_Init(void) {
    uart_rx_idx = 0;
    uart_cmd_ready = 0;
    HAL_UART_Receive_IT(&huart1, (uint8_t *)&uart_rx_buf[0], 1);
}

void UART_Console_Printf(const char *fmt, ...) {
    char buf[128];
    va_list args;
    va_start(args, fmt);
    int len = vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);
    if (len > 0) {
        HAL_UART_Transmit(&huart1, (uint8_t *)buf, len, 100);
    }
}

uint8_t UART_Console_IsCmdReady(void) { return uart_cmd_ready; }
char*   UART_Console_GetCmd(void)     { return uart_rx_buf; }
void    UART_Console_AckCmd(void)     { uart_rx_idx = 0; uart_cmd_ready = 0; }

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == USART1) {
        char ch = uart_rx_buf[uart_rx_idx];
        if (ch == '\r' || ch == '\n') {
            if (uart_rx_idx > 0) {
                uart_rx_buf[uart_rx_idx] = '\0';
                uart_cmd_ready = 1;
            }
        } else if (uart_rx_idx < 64 - 1) {
            uart_rx_buf[uart_rx_idx++] = ch;
            HAL_UART_Transmit(&huart1, (uint8_t *)&ch, 1, 10);
        }
        HAL_UART_Receive_IT(&huart1, (uint8_t *)&uart_rx_buf[uart_rx_idx], 1);
    }
}
